    assert.match(source, /TIKTOK_CARD_SORT_FIELD_OPTIONS/);
    assert.match(source, /compareTikTokCardFilterEntries/);
    assert.match(source, /近24小时/);
    assert.match(source, /近7天/);
    assert.match(source, /不限时间/);
    assert.match(source, /发布日期从/);
    assert.match(source, /发布日期到/);
    assert.doesNotMatch(source, /每小时点赞量最大/);
    assert.match(source, /非英内容/);
    assert.match(source, /时长<1分/);
    assert.match(source, /发布>1天/);
    assert.match(source, /createTikTokCardMetricsOverlay/);
    assert.match(source, /getTikTokVideoMetricItems/);
    assert.match(source, /getTikTokWarningLabels/);
