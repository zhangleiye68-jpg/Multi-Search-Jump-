    assert.match(css, /\.msj-tiktok-card-filter-quick\s*{/);
    assert.match(css, /\.msj-tiktok-card-filter-section-title\s*{/);
    assert.match(css, /\.msj-tiktok-card-filter-chip\.is-active\s*{[\s\S]*background:\s*#16a34a/);
